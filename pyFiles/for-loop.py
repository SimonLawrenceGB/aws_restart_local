# Displays the string "Count to 10!" in the terminal.
print("Count to 10!")
# The for loop uses x to iterate between the integer values in the 'range()' function.
# In this case that would be 1 through 10 because the last integer is excluded in the 'range()' function.
# Then each iteration is displayed in the terminal by the print() function.
for x in range(0, 11):
    print(x)
